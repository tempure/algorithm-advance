void solve() {
    int n, m;
    cin >> n >> m;
    vi A(n);
    vi B(m);
    for (auto &x : A) cin >> x;
    for (auto &x : B) cin >> x;
    sort(all(A));
    sort(all(B));
    int res = 0x3f3f3f3f;
    int i = 0, j = 0;
    while (i < n && j < m) {
        res = min(res, abs(A[i] - B[j]));
        if (A[i] > B[j]) j++;
        else i++;
    }
    cout << res << endl;
}

int main() {
    int t = 1;
    // cin >> t;
    while (t --) solve();
    return 0;
}